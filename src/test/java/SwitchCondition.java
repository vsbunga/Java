public class SwitchCondition

{
    public static void main(String[] args)
    {
        int a = 2;

        switch (a)
        {
            case 1:
                System.out.println("Go");
                break;
            case 2:
                System.out.println("Stop");
                break;
            default:
                System.out.println("wait");
                break;
        }
    }

}
