import javax.security.auth.Subject;

public class arrays1
{
    public static void main(String[] args)
    {
        String[] subjects = new String[5];
        for (String x : subjects ){
            System.out.print(x+" | ");
        }
        System.out.println();

        float[] Salaries = new float[5];
        for (float x : Salaries){
            System.out.print(x+" | ");
        }
        System.out.println();

        boolean[] stats = new boolean[5];
        for(boolean x : stats){
            System.out.print(x+" | ");
        }




    }
}
